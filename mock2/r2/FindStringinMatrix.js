// Find String In Matrix Ended
// Description

// You're given a string S and character matrix of size N*M. Find the number of times S occurs in the matrix horizontally or vertically

// Horizontally -> left to right

// Vertically -> top to bottom


// Input
// Input Format

// The first line contains the dimensions of the matrix N and M

// The second line contains the string S

// The next N lines contains M characters each

// Constraints

// 1<=N<=500

// 1<=M<=500

// 1<=Length of S<=500


// Output
// Print the number of times S occurs in the matrix horizontally or vertically.


// Sample Input 1 

// 3 4
// ab
// abad
// eakg
// abgh
// Sample Output 1

// 3
// Hint

// The three occurrences of "ab" are : (0,0) to (0,1)  , (1,1) to (2,1) and (2,0) to (2,1)