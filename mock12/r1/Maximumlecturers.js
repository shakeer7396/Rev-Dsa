// Maximum Lectures Ended
// Description

// The city museum is holding a special event. There will N different lectures. If the starting and ending time of each lecture is known what is the maximum number of lectures that can be entirely attended by a person


// Input
// Input Format

// The first line contains the number of lectures N

// The next N lines contains start time and end time of the respective lecture.

// Constraints

// 1<=N<=2*10^5

// 1<=start,end<=10^9


// Output
// Print the maximum number of lectures that a person can attend.


// Sample Input 1 

// 3
// 3 5
// 4 9
// 5 8
// Sample Output 1

// 2
// Hint

// The two lectures that can be attended are 1 and 3.